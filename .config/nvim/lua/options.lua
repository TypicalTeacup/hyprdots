-- 4 space tabs
vim.opt.expandtab = true
vim.opt.tabstop = 4
vim.opt.softtabstop = 4
vim.opt.shiftwidth = 4

-- line numbers
vim.opt.number = true
vim.opt.numberwidth = 5

-- yank to clipboard
vim.opt.clipboard = "unnamedplus"

-- scrolloff
vim.opt.scrolloff = 8
vim.opt.sidescrolloff = 8

-- break on word
vim.opt.linebreak = true

-- cursorline
vim.opt.cursorline = true
